CREATE PROCEDURE `itms`.`AUTO_DROP_PARTITION`(IN in_schema VARCHAR ( 20 ),
    IN in_table_name VARCHAR ( 20 ),
    IN in_days INTEGER)
BEGIN
  DECLARE str_day VARCHAR(64);
  DECLARE _err INT(1);
  DECLARE done INT DEFAULT 0;
  DECLARE par_name VARCHAR(64);

  DECLARE cur_partition_name CURSOR FOR
  SELECT partition_name
  FROM INFORMATION_SCHEMA.PARTITIONS
  WHERE TABLE_SCHEMA = in_schema AND table_name = in_table_name
  ORDER BY partition_ordinal_position;

  DECLARE CONTINUE HANDLER FOR SQLEXCEPTION, SQLWARNING, NOT FOUND SET _err = 1;
  DECLARE CONTINUE HANDLER FOR SQLSTATE '02000' SET done = 1;

  SET str_day = DATE_FORMAT(DATE_SUB(NOW(),INTERVAL overdue_days DAY), '%Y%m%d');

  OPEN cur_partition_name;
  REPEAT
    FETCH cur_partition_name INTO par_name;
    IF (str_day > SUBSTRING(par_name, 2)) THEN
      SET @alter_sql = CONCAT('alter table ', in_table_name, ' drop PARTITION ', par_name);
      PREPARE stmt1 FROM @alter_sql;
      EXECUTE stmt1;
    END IF;
  UNTIL done END REPEAT;
  CLOSE cur_partition_name;
END
;;
delimiter ;

