delimiter ;;
CREATE PROCEDURE `itms`.`AUTO_EXCHANGE_PARTITION`(IN in_table_name VARCHAR ( 20 ))
BEGIN
    /* 事务回滚，其实放这里没什么作用，ALTER TABLE是隐式提交，回滚不了的。*/
    declare exit handler for sqlexception rollback;
    start TRANSACTION;

    -- 归档表和临时表
    SET @table_t=concat(in_table_name, '_T');
    SET @table_tmp=concat(in_table_name, '_TMP');

    -- 定义交换分区变量（昨天）
    select concat('p', DATE_FORMAT(DATE_SUB(NOW(),INTERVAL 1 DAY),'%Y%m%d')) into @exchangePatition;

    -- 清空临时表
    SET @s1=concat('TRUNCATE TABLE ',@table_tmp);
    PREPARE stmt1 FROM @s1;
    EXECUTE stmt1;
    DEALLOCATE PREPARE stmt1;

    -- 先把BUSI_OPER某天分区的数据交换到BUSI_OPER_TMP表
    -- 再把BUSI_OPER_TMP表的数据交换到BUSI_OPER_T表某天分区
    SET @s2=concat('alter table ',in_table_name,' exchange partition ',@exchangePatition,' with table ',@table_tmp);
    PREPARE stmt2 FROM @s2;
    EXECUTE stmt2;
    DEALLOCATE PREPARE stmt2;

    SET @s3=concat('alter table ',@table_t,' exchange partition ',@exchangePatition,' with table ',@table_tmp);
    PREPARE stmt3 FROM @s3;
    EXECUTE stmt3;
    DEALLOCATE PREPARE stmt3;

    -- 这个时候统计信息没有更新，需要先更新统计信息，再查看分区内数据
    SET @s4=concat('analyze table ',in_table_name);
    PREPARE stmt4 FROM @s4;
    EXECUTE stmt4;
    DEALLOCATE PREPARE stmt4;

    SET @s5=concat('analyze table ',@table_t);
    PREPARE stmt5 FROM @s5;
    EXECUTE stmt5;
    DEALLOCATE PREPARE stmt5;

END
;;
delimiter ;

