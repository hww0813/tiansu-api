delimiter ;;
CREATE PROCEDURE `itms`.`AUTO_ADD_PARTITION`(IN in_schema VARCHAR ( 20 ),
    IN in_table_name VARCHAR ( 20 ),
    IN in_days INTEGER)
BEGIN
    /* 事务回滚，其实放这里没什么作用，ALTER TABLE是隐式提交，回滚不了的。*/
    declare exit handler for sqlexception rollback;
    start TRANSACTION;

    SELECT REPLACE( partition_name, 'p', '' ) into @last_pname FROM INFORMATION_SCHEMA.PARTITIONS WHERE TABLE_SCHEMA = in_schema AND table_name = in_table_name ORDER BY partition_ordinal_position DESC LIMIT 1;
    /* 打印日志 */
    SELECT concat('last partition name is ', @last_pname);

    -- 定义开始循环时间变量
    select STR_TO_DATE(@last_pname, '%Y%m%d') into @start_date;
    select DATE_ADD(NOW(),INTERVAL in_days DAY) into @end_data;

    -- 循环创建分区表
    WHILE datediff(@start_date, @end_data) <= 0
    DO
        select DATE_FORMAT(@start_date,'%Y%m%d') into @cur_date_str;
        set @cur_partition_name = concat('p',@cur_date_str);
        SELECT count(partition_name) into @pcnt FROM INFORMATION_SCHEMA.PARTITIONS WHERE TABLE_SCHEMA = in_schema AND table_name = in_table_name AND partition_name = @cur_partition_name;
        IF @pcnt = 0 THEN
	    /* 修改表，在最大分区的后面增加一个分区，时间范围加半个月 */
	    SET @s1=concat('ALTER TABLE ',in_table_name,' ADD PARTITION (PARTITION ',@cur_partition_name,' VALUES LESS THAN (TO_DAYS (''',date(@cur_date_str),''')))');
            PREPARE stmt2 FROM @s1;
	    EXECUTE stmt2;
	    DEALLOCATE PREPARE stmt2;
        END IF;

        -- 给日期+1天
        set @start_date = DATE_ADD(@start_date,INTERVAL 1 DAY);
    END WHILE;

END
;;
delimiter ;

