CREATE PROCEDURE `itms`.`iniSstatistics`()
BEGIN
	DECLARE srcIp BIGINT(11);
	DECLARE userNameCache VARCHAR(50);
	DECLARE dstCode VARCHAR(50);
	DECLARE stampTime DATE;
	DECLARE operAction INT(1);
	DECLARE s INT DEFAULT 0;

	DECLARE report CURSOR FOR SELECT src_ip,username,dst_code,stamp,ACTION FROM busi_oper  ORDER BY stamp ASC;
	DECLARE CONTINUE HANDLER FOR NOT FOUND SET s=1;
	OPEN report;
	REPEAT
		FETCH report INTO srcIp,userNameCache,dstCode,stampTime,operAction;
		IF s=0 THEN
			INSERT INTO busi_statistics(src_ip,username,dst_code,stamp,ACTION) VALUES(srcIp,userNameCache,dstCode,stampTime,operAction) ON DUPLICATE KEY UPDATE COUNT = COUNT + 1;
		END IF;
	UNTIL s=1 END REPEAT;

	CLOSE report;



END
;;
delimiter ;

